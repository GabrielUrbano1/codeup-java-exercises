public class StringExercise {
    public static void main(String[] args) {
        // Example 1
        String example1 = "We don't need no education\nWe don't need no thought control";
        System.out.println(example1);

        // Example 2
        String example2 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(example2);

        // Example 3
        String example3 = "In windows, the main drive is usually C:\\";
        System.out.println(example3);

        // Example 4
        String example4 = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(example4);
    }
}
